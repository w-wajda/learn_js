let number9 = 0;

//inkrementacja

console.log(number9++);
console.log(number9);

let number10 = 2

console.log(++number10); //preinkrementacja - zwiększ wartość o 1
console.log(number10);

//dekrementacja

let number11 = 5;

console.log(number11--);
console.log(number11);

let number12 = 8

console.log(--number12); // predekrementacja - mniejsz wartość o 1
console.log(number12);
